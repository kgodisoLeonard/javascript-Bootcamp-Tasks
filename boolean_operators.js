// Comparison operators
let comp1 = 10 > 5;
let comp2 = 3 === "3";

console.log("10 > 5 is", comp1);
console.log("3 === '3' is", comp2);

// x and y comparison
let x = 8;
let y = 12;

console.log("x > y:", x > y);
console.log("x <= y:", x <= y);
console.log("x === y:", x === y);
console.log("x !== y:", x !== y);

// Logical operators
let a = true;
let b = false;

console.log("a AND b:", a && b);
console.log("a OR b:", a || b);
console.log("NOT a:", !a);

// Assignment operators
let p = 10;
p += 5;
console.log("p += 5:", p);
p -= 3;
console.log("p -= 3:", p);
p *= 2;
console.log("p *= 2:", p);
p /= 4;
console.log("p /= 4:", p);
p %= 3;
console.log("p %= 3:", p);